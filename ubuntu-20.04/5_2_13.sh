function cis_test_run()
{
	cmd=$(sshd -T | grep ciphers | grep -E "3des-cbc|aes128-cbc|aes192-cbc|aes256-cbc|arcfour|arcfour128|arcfour256|blowfish-cbc|cast128-cbc|rijndael-cbc@lysator.liu.se")
	[ -n "$cmd" ] && return 1

	return 0
}